export const getImage = (i) => {
  switch (i) {
    case "月":
      return "/schedules/7080.png";
    case "水":
      return "/schedules/disco_night.png";
    case "金":
      return "/schedules/maharaja_night.png";
    case "土":
      return "/schedules/global.png";
    default:
      break;
  }
};
